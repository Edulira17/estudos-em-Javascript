function operacoes(num1, num2) {
  let soma = num1 + num2
  console.log('A soma entre os valores é:', soma)

  let subtracao = num1 - num2
  console.log('A subtração entre os valores é:', subtracao)

  let multiplicacao = num1 * num2
  console.log('A multiplicação entre os valores é:', multiplicacao)

  let divisao = num1 / num2 
  console.log('A divisão entre os valores é:', divisao)
} 

console.log(operacoes(20, 4))