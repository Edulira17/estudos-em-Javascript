let soma = (num1, num2) => num1 + num2
let subtracao = (num1, num2) => num1 - num2
let multiplicacao = (num1, num2) => num1 * num2
let divisao = (num1, num2) => num1 / num2


console.log(`A soma entre os números é ${soma(5, 5)}.`)
console.log(`A subtração entre os números é ${subtracao(7, 4)}.`)
console.log(`A multiplicação entre os números é ${multiplicacao(23, 4)}.`)
console.log(`A divisão entre os números é ${divisao(28, 6).toFixed(2)}`)
